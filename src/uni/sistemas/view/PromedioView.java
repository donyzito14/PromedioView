    private void btnProcesarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcesarActionPerformed
        double prom = 0;
        switch(cboTipo.getSelectedIndex()){
            case 1 :
                if(txtParcial.getText().length()> 0 && txtFinal.getText().length() > 0){
                    prom = obj.promedio(Integer.parseInt(txtParcial.getText()),
                        Integer.parseInt(txtFinal.getText()));                
                }else {
                    JOptionPane.showMessageDialog(this,"Falta notas");
                }
                break;
            case 2 : 
                prom = obj.promedio(Integer.parseInt(txtParcial.getText()),
                        Integer.parseInt(txtFinal.getText()),
                        Integer.parseInt(txtPractica.getText()));
                break;
            case 3 : 
                prom = obj.promedio(Integer.parseInt(txtParcial.getText()),
                        Integer.parseInt(txtFinal.getText()),
                        Integer.parseInt(txtPractica.getText()),
                        Integer.parseInt(txtLaboratorio.getText()));
                break;
            case 4 : 
                prom = obj.promedio(Integer.parseInt(txtParcial.getText()),
                        Integer.parseInt(txtFinal.getText()),
                        Integer.parseInt(txtPractica.getText()),
                        Integer.parseInt(txtLaboratorio.getText()),
                        Integer.parseInt(txtProyecto.getText()));
                break;
        }
        txtPromedio.setText(prom+"");
    }//GEN-LAST:event_btnProcesarActionPerformed

